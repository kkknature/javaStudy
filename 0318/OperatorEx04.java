public class OperatorEx04 {
    public static void main(String[] args) {
        
        // +
        // 문자열 연결 연산자
        System.out.println( "Hello " + "Java" );

        // 결과 예측 - 확실
        System.out.println( ( "1" + 1 ) + 1 );
        System.out.println( 1 + "1" + 1 );
        System.out.println( 1 + 1 + "1" );

    }
}
