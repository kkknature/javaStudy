public class OperatorEx01 {
    public static void main(String[] args) {
        
        // 나머지 : % = 정수
        // 짝수 / 배수

        // 1 % 2 = 1
        // 2 % 2 = 0
        // 3 % 2 = 1
        // 4 % 2 = 0
        // 5 % 2 = 1
        
        System.out.println( 1 % 2 );
        System.out.println( 2 % 2 );
        System.out.println( 3 % 2 );
        System.out.println( 4 % 2 );

    }
}
