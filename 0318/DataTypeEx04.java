public class DataTypeEx04 {
    public static void main(String[] args) {

        // 문자열
        String str1 = "Hello 안녕";
        System.out.println( str1 );

        String str2 = "Hello \n 안녕";
        System.out.println( str2 );

        //String str3 = "Hello '안녕'";
        String str3 = "Hello \"안녕\"";
        System.out.println( str3 );

        
    }
}
