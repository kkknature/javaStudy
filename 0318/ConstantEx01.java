public class ConstantEx01 {
    public static void main(String[] args) {

        // 변수의 선언
        int vnum = 10;
        System.out.println( vnum );
        vnum = 20;
        System.out.println( vnum );

        final int C_NUM = 10;
        System.out.println( C_NUM );
        //C_NUM = 20;
        System.out.println( C_NUM );

    }
}
