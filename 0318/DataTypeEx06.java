public class DataTypeEx06 {
    public static void main(String[] args) {
        
        // 실수
        // float / double

        // 2.5
        //int i = 2.5;
        //System.out.println( i );

        float f = 2.5f;
        System.out.println( f );
        double d = 2.5;
        System.out.println( d );

        // 지수
        // 1000 = 1.0 x 10e3
        double d2 = 1.0e3;
        System.out.println( d2 );

    }
}
