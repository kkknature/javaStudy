public class SwitchEx01 {
    public static void main(String[] args) {
        int data = 20;

        switch(data) {
            case 10 :
                System.out.println("10");
                break;
            case 20 :
                System.out.println("20");
                break;
            case 30 :
                System.out.println("30");
                break;
            default :
                System.out.println("기타");
        }
    }
}
